package ex_10_For_Loop;

public class Lab_098_For_Loop {
    public static void main(String[] args) {
        //For Loop
        //Help you to repeat the block of codes

        //Initialization -> Condition -> Updation(Incre/decre)
        //ICU

        for(int i=0; i<10; i++){
            System.out.println(i);

            //it will get executed 10 times from 0 to 9
        }
    }
}
