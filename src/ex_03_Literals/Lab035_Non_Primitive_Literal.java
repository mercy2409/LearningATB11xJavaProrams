package ex_03_Literals;

public class Lab035_Non_Primitive_Literal {
    public static void main(String[] args) {

        //Primitive data type - defined by Java groups
        //Min, Max or Size defined
        byte b = 10;  //byte -> 8 bits (memory how much space it will take)
        int age = 65; // 4 byte -> 32 bits


        //Non primitive (Defined by the users)
        // No Min, Max or Size
        //Byte size - 8, Bits - 64
        String name = "Mercy"; //String is a bunch of characters
        int [] arrays_of_items =new int [10];
        //Non primitive data type contains lot of them Arrays - String, Arrays, Classess, enum ...




    }
}
