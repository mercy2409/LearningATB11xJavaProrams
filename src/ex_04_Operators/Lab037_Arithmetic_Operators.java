package ex_04_Operators;

public class Lab037_Arithmetic_Operators {
    public static void main(String[] args) {

        // Arithmetic Operators
        // + Addition
        // - Subraction
        // * Multiplication
        // / Division
        // % Modulus

        int a = 20;
        int b = 3;
        float c = 3f; // 3.0f is also applicable

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
        System.out.println(a/c);
        System.out.println("a+b");
    }
}
